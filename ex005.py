# Faça um programa que leia um número inteiro e mostre na tela o sucessor e seu antecessor.
numero_inteiro = int(input('Digite um número: '))
sucessor = numero_inteiro + 1
antecessor = numero_inteiro - 1
print(f'Ótimo! O antecessor de {numero_inteiro} é {antecessor} e o seu sucessor é {sucessor}!')
